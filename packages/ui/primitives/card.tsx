import { forwardRef, type HTMLAttributes } from "react";
import { cn } from "../lib/cn";

/**
 * Card — surface that sits one level above the page background. Uses the
 * `--card` token (white in light, near-black in dark) so it stays visible
 * regardless of theme. `text-card-foreground` ensures contrast even
 * inside a tinted page background like `bg-muted`.
 */
export const Card = forwardRef<HTMLDivElement, HTMLAttributes<HTMLDivElement>>(
  ({ className, ...props }, ref) => (
    <div
      ref={ref}
      className={cn(
        "rounded-lg border bg-card text-card-foreground shadow-sm",
        className,
      )}
      {...props}
    />
  ),
);
Card.displayName = "Card";

export const CardHeader = forwardRef<
  HTMLDivElement,
  HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn("flex flex-col gap-1.5 p-6", className)}
    {...props}
  />
));
CardHeader.displayName = "CardHeader";

